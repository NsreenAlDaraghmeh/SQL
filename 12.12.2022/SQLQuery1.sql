create table customers(
id int primary key ,
firstname varchar(20),
lastname varchar(20),
);
select*from customers ;


create table orders(
id int primary key ,
price float,
total float,
);
select*from orders ;


create table products(
id int primary key ,
name varchar(20),
price float ,
);
select*from products ;


create table Employees(
id int primary key ,
firstname varchar(20),
lastname varchar(20),
);
select*from Employees ;